export function getOrderById(id: string): Order | undefined {
	return mockOrders.find((o) => o.id === id);
}

export function getOrdersByStatus(status: Order["fulfillmentStatus"]): Order[] {
	return mockOrders.filter((o) => o.fulfillmentStatus === status);
}

export function getDashboardStats(): DashboardStats {
	const totalRevenue = mockOrders.reduce((sum, order) => sum + order.total, 0);
	const ordersToFulfill = mockOrders.filter(
		(o) =>
			o.fulfillmentStatus === "unfulfilled" ||
			o.fulfillmentStatus === "in_progress",
	).length;
	const pendingPayments = mockOrders.filter(
		(o) => o.paymentStatus === "pending",
	).length;

	return {
		totalOrders: mockOrders.length,
		totalRevenue,
		totalProducts: 6, // From mock-products
		ordersToFulfill,
		pendingPayments,
	};
}
